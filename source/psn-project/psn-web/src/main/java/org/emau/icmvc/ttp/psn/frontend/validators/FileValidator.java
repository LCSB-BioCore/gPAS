import java.util.ResourceBundle;

import javax.faces.application.FacesMessage;
import javax.faces.bean.ManagedBean;
import javax.faces.bean.RequestScoped;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.validator.Validator;
import javax.faces.validator.ValidatorException;

import org.primefaces.model.UploadedFile;

@ManagedBean(name = "fileValidator")
@RequestScoped
public class FileValidator implements Validator {

	@Override
	public void validate(FacesContext context, UIComponent component, Object value) throws ValidatorException {
		ResourceBundle messages = ResourceBundle.getBundle("messages");
		UploadedFile file = (UploadedFile) value;
		if (!(file.getContentType().equals("text/csv") || file.getContentType().equals("text/plain"))) {
			throw new ValidatorException(new FacesMessage(FacesMessage.SEVERITY_ERROR, messages.getString("file.error.invalidType"), ""));
		}

	}

}
